class RGB {
  int _red = 0;
  int _blue = 0;
  int _green = 0;

  int get red {
    return _red;
  }

  set red(int red) {
    this._red = red;
  }

  int get blue {
    return _blue;
  }

  set blue(int blue) {
    this._blue = blue;
  }

  int get green {
    return _green;
  }

  set green(int green) {
    this._green = green;
  }
}
